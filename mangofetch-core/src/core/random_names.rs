pub fn get_random_name() -> &'static str {
    use std::time::{SystemTime, UNIX_EPOCH};
    let seed = SystemTime::now()
        .duration_since(UNIX_EPOCH)
        .unwrap_or_default()
        .as_nanos() as usize;
    RANDOM_NAMES[seed % RANDOM_NAMES.len()]
}
